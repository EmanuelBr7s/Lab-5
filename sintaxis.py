# Imprimir un mensaje en la consola
print("¡Hola, bienvenido a mi programa!")

# Declarar variables de diferentes tipos
numero_entero = 10           # Variable de tipo int
numero_decimal = 3.14       # Variable de tipo float
mensaje = "El resultado es"  # Variable de tipo str

# Realizar operaciones simples
suma = numero_entero + numero_decimal
resta = numero_entero - numero_decimal
producto = numero_entero * numero_decimal
division = numero_entero / numero_decimal

# Concatenar cadenas
resultado = f"{mensaje}: Suma = {suma}, Resta = {resta}, Producto = {producto}, División = {division}"

# Imprimir el resultado
print(resultado)
